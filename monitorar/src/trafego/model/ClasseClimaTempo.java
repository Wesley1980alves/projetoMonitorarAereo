package trafego.model;

/**
 *
 * @author wesley
 */
public class ClasseClimaTempo {

    private String localizacao;
    private String nuvens;
    private String temperatura;
    private String sensacao;
    private String umidadeAr;
    private String pressao;
    private String vento;

    public String getLocalizacao() {
        return localizacao;
    }

    public void setLocalizacao(String localizacao) {
        this.localizacao = localizacao;
    }

    public String getNuvens() {
        return nuvens;
    }

    public void setNuvens(String nuvens) {
        this.nuvens = nuvens;
    }

    public String getTemperatura() {
        return temperatura;
    }

    public void setTemperatura(String temperatura) {
        this.temperatura = temperatura;
    }

    public String getSensacao() {
        return sensacao;
    }

    public void setSensacao(String sensacao) {
        this.sensacao = sensacao;
    }

    public String getUmidadeAr() {
        return umidadeAr;
    }

    public void setUmidadeAr(String umidadeAr) {
        this.umidadeAr = umidadeAr;
    }

    public String getPressao() {
        return pressao;
    }

    public void setPressao(String pressao) {
        this.pressao = pressao;
    }

    public String getVento() {
        return vento;
    }

    public void setVento(String vento) {
        this.vento = vento;
    }

}
