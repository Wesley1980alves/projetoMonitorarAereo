    private void txtCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtCadastrarActionPerformed
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            try {
                Connection conectado = DriverManager.getConnection("jdbc:mysql://localhost:3306/trafegoAereo", "root",
                        "");
                PreparedStatement st = conectado.prepareStatement("insert into comandante values(?,?,?,?,?,?)");
                st.setString(1, txtPiloto.getText());
                st.setString(2, txtCopiloto.getText());
                st.setString(3, txtControlador.getText());
                st.setString(4, txtCod_dac.getText());
                st.setString(5, txtIdentificacao.getText());
                st.setString(6, txtHoras_voo.getText());
                
                st.executeUpdate();
                JOptionPane.showMessageDialog(null, " COMANDANTE INSERIDO NO SISTEMA ");
                
                txtPiloto.setText("");
                txtCopiloto.setText("");
                txtControlador.setText("");
                txtCod_dac.setText("");
                txtIdentificacao.setText("");
                txtHoras_voo.setText("");
                txtPiloto.requestFocus();
                
            } catch (SQLException ex) {
                JOptionPane.showConfirmDialog(null, ex.getMessage());
            }
        } catch (ClassNotFoundException ex) {
            JOptionPane.showConfirmDialog(null, ex.getMessage());
            
        }
    }//GEN-LAST:event_txtCadastrarActionPerformed

