    private void txtPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtPesquisarActionPerformed
        String codigo;
        codigo = pesquisarCod_dac.getText();
        ClasseComandanteDao comandanteDao = new ClasseComandanteDao();
        boolean status = comandanteDao.conectar();
        if (status == true) {
            ClasseComandante classeComandante = comandanteDao.consultar(codigo);
            if (classeComandante == null) {
                JOptionPane.showConfirmDialog(null, "COMANDANTE NÃO ENCONTRADO NO BANCO DE DADOS ");
            } else {
                txtPiloto.setText(classeComandante.getNome());
                txtCopiloto.setText(classeComandante.getCo_piloto());
                txtControlador.setText(classeComandante.getControladorAeronave());
                txtCod_dac.setText(classeComandante.getCod_dac());
                txtIdentificacao.setText(classeComandante.getIdentificacaolicenca());
                txtHoras_voo.setText(classeComandante.getHoras_voo());
                
            }
            pesquisarCod_dac.setText("");
            comandanteDao.desconectar();
        } else {
            JOptionPane.showMessageDialog(null, "OPS!!!!FALHA NA CONEXÃO COM BANCO DE DADOS ");
        }
    }//GEN-LAST:event_txtPesquisarActionPerformed

